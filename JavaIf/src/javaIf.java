public class javaIf {
    public static void main(String[] args) {
        int numero = 4;

        if (numero == 0){
            System.out.println("El numero es cero")
            ;
        } else if (numero <= 0) {
            System.out.println("El número es negativo");
        } else {
            System.out.println("El número es positivo");
        }

    }
}
